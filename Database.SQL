CREATE TABLE ADMIN(ID VARCHAR(20) PRIMARY KEY,
		PASSWORD VARCHAR(30));

CREATE TABLE STUDENT(USN VARCHAR(15) PRIMARY KEY, NAME VARCHAR(30), SEM INT, EMAIL VARCHAR(30), NOI INT, PASSWORD VARCHAR(30));

create table activity(usn varchar(30), name varchar(30), point int, foreign key(usn) references student(usn) on delete cascade);


CREATE TABLE TEACHER(ID VARCHAR(15) PRIMARY KEY,NAME VARCHAR(15),PASSWORD VARCHAR(15));

CREATE TABLE INTERNDETAIL(USN VARCHAR(15),INTERNSHIPID VARCHAR(15) UNIQUE,NAME VARCHAR(15),SEM INT,EMAIL VARCHAR(30),CNAME VARCHAR(15),ROLE VARCHAR(15),DURATION INT,FOREIGN KEY(USN) REFERENCES STUDENT(USN) ON DELETE CASCADE);

CREATE TABLE OFFERINTERN(INTERNSHIPID VARCHAR(15) UNIQUE,CNAME VARCHAR(15),ROLE VARCHAR(15),DURATION INT,STIPEND INT,PRIMARY KEY(INTERNSHIPID),FOREIGN KEY(CNAME) REFERENCES COMPANY(NAME) ON DELETE CASCADE);

CREATE TABLE AVAILINTERN(INTERNSHIPID VARCHAR(15),CNAME VARCHAR(15),USN VARCHAR(15),SNAME VARCHAR(15),SEM INT,FOREIGN KEY(INTERNSHIPID) REFERENCES OFFERINTERN(INTERNSHIPID) ON DELETE CASCADE,FOREIGN KEY(USN) REFERENCES STUDENT(USN) ON DELETE CASCADE,FOREIGN KEY(CNAME) REFERENCES COMPANY(NAME) ON DELETE CASCADE);

CREATE TABLE HIRINGOFFER(INTERNSHIPID VARCHAR(15),USN VARCHAR(15),NAME VARCHAR(15),SEM INT,FOREIGN KEY(INTERNSHIPID) REFERENCES OFFERINTERN(INTERNSHIPID) ON DELETE CASCADE,FOREIGN KEY(USN) REFERENCES STUDENT(USN) ON DELETE CASCADE);

create table notification(usn varchar(15),foreign key(usn) references student(usn),internshipid varchar(15),foreign key(internshipid) references offerintern(internshipid),cname varchar(15),foreign key(cname) references company(name),role varchar(15),stipend int);